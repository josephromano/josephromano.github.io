function playdiatonicscale(temp)
%
% play diatonic scale in major interval order
%
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
dur = 1; % duration in seconds

playnote('C',4,temp,dur);
playnote('D',4,temp,dur);
playnote('E',4,temp,dur);
playnote('F',4,temp,dur);
playnote('G',4,temp,dur);
playnote('A',4,temp,dur);
playnote('B',4,temp,dur);
playnote('C',5,temp,dur);

return
